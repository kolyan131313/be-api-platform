    /**
     * Create a new user
     * @param $data
     * @return User
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function createNewUser($data)
    {
        $user = new User();
        $user->setEmail($data['email'])
            ->setPassword($data['password']);

        $this->getEntityManager()->persist($user);
        $this->getEntityManager()->flush($user);

        return $user;
    }

